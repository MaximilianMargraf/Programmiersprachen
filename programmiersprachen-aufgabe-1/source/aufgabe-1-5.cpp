#include <iostream>

int main()
{
	// Das Initialisieren einer Variable bezeichnet die Deklaration und Definition.
	int a = 10;
	// Daher kann eine Variable nur einmal initialisiert werden.
	// Initialisiert man die Variable nicht, so hat sie einen zufälligen Wert.

	// Eine Zuweisung erfolgt später und sieht wie folgt aus:
	int b;
	b = 15;
	b = 20;
	// Eine Variable kann beliebig oft neue Werte zugewiesen bekommen.

	return 0;
}